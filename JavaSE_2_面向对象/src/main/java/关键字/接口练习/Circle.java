package 关键字.接口练习;

public class Circle {
	private Double radius;
	//构造器
	
	public Circle(Double radius) {
		super();
		this.radius = radius;
	}
	public Circle() {
		super();
	}
	//get
	public Double getRadius() {
		return radius;
	}
	//set
	public void setRadius(Double radius) {
		this.radius = radius;
	}

}
