package HelloWorld;

//public 代表，全局的含义
//采用 class 来定义类的名称
//HelloWorld.HelloWorld 是类的名称，注意类的名称区分大小写
public class HelloWorld {
    //static, 表示是静态的
    //void, 表示没有返回值
    //main, 入口方法，方法的参数也不能改变
    public static void main(String[] args) {
        //在控制台里面打印一行信息，信息内容是Hello World!
        System.out.println("Hello World!");
    }
}